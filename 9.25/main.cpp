#include <iostream>

using namespace std;

void f(int &, int &); //선언

int main() {

    int a{1};
    int b{2};
    // [문제] 함수 f를 호출하였다. a와 b 값이 교환 되도록 하라. - 선언, 정의 , 호출
    // 함수 호출
    f(a, b); //호출

    cout << a << "," << b << endl; // 화면출력 - 2, 1}

}

void f(int &a, int &b) { //정의문
    int temp;
    temp = a;
    a = b;
    b = temp;
}