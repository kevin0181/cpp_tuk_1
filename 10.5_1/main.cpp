#include <iostream>

using namespace std;

int main() {
    //그걸 p라는 이름의 메모리 주소에 100으로 줌
    int *p = new int{100}; // 메모리 4byte를 free store에 확보해줌 누군가가; 그 내용을 100이라고 해놓음.
    cout << *p << endl;
    //-----------------------------------------------------------------
    // c++에서 추천하지 않아서 쓰레기 예제라그럼ㅋ.. 아마 포인터 변수 써서그런듯?
    int *p2;
    p2 = new int; // 1. 메모리 요청 (시스템에게)

    cout << *p2 << endl; // 2. 메모리 access (지금은 읽기)

    delete p2; // 3. 메모리 반환
    // ---------------------
}
